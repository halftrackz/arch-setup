SERVICES=(dhcpv6-client ipp mdns samba-client ssh)
for svc in "${SERVICES[@]}"; do
    firewall-cmd --permanent --zone=$ZONE --add-service="$svc"
done

# в”Ђв”Ђ Custom TCP ports (9942-9945) в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
for port in 9942 9944 9945; do
    firewall-cmd --permanent --zone=$ZONE --add-port="${port}/tcp"
done

# в”Ђв”Ђ Custom UDP ports (9942-9945) в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
for port in 9942 9944 9945; do
    firewall-cmd --permanent --zone=$ZONE --add-port="${port}/udp"
done

# в”Ђв”Ђ Steam/Game TCP ports (27014-27050) в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
for port in $(seq 27014 27050); do
    firewall-cmd --permanent --zone=$ZONE --add-port="${port}/tcp"
